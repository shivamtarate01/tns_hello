package tns_hello;

public class IdentifierDemo {

	public static void main(String[] args) {
		
		//name of variable can start with $ and _ symbol only otherwise
		//only characters which are not capital
		int $num = 50;
		System.out.println("The value of the number is :"+ $num);
		
		
		
		//string should be always written with double quotes
		//string can be of any size
		String studentname = "Shivam";
		System.out.println("The name of student is :"+ studentname);
	}

}
